const fruta = "abacaxi";

switch (fruta) {
    case "laranja":
        console.log("A fruta é laranja");
        break;
    case "maca":
        console.log("A fruta é maça");
        break;
    case "uva":
        console.log("A fruta é uva");
        break;
    default:
        console.log("A fruta não é laranja, maça ou uva");
        break;
}